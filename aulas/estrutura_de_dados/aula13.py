# sets com strings

set1 = {"banana", "maçã", "laranja"}
set2 = ["maçã", "uva", "abacaxi"]
set3 = ["pera", "banana", "kiwi", "maçã"]

set4 = set1.union(set2)  # União
set5 = set1.intersection(set3)  # Interseção
set6 = set1.difference(set3)  # Diferença
set7 = set1.symmetric_difference(set2)  # Diferença simétrica