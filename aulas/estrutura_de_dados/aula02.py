# Funções dentro de listas

cidades = ['São Paulo', 'Rio de Janeiro', 'Belo Horizonte']

# Adiciona um elemento no final da lista
cidades.append('Curitiba')
cidades.append('Salvador')

# Remove o elemento especificado da lista
cidades.remove('Belo Horizonte')

# Insere um elemento na posição especificada
cidades.insert(1, 'Recife')

# Remove o elemento na posição especificada
cidades.pop(0)

# Ordena a lista
cidades.sort()

print(cidades)